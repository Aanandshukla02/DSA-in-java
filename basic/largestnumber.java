import java.util.*;

public class largestnumber {
    public static void main(String[] args) {
        // Initialize two numbers
        int a = 10;
        int b = 6;

        // Compare the numbers and print the largest one
        if (a > b) {
            System.out.println(a + " is the largest.");
        } else {
            System.out.println(b + " is the largest.");
        }
    }
}
