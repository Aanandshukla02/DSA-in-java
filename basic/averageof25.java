public class averageof25 {
    public static void main(String[] args) {
        // Initialize scores for three subjects
        float bio = 15f;
        float phy = 20f;
        float che = 5f;

        // Calculate the average
        float average = (bio + phy + che) / 3;

        // Print the average
        System.out.println("Average Score: " + average);
    }
}
