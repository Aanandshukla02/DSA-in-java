public class even9 {
    public static void main(String[] args) {
        // Initialize variable i with the value 9
        for (int i = 9; i <= 100; i++) {
            // Check if i is even
            if (i % 2 == 0) {
                // Print even numbers
                System.out.print(" " + i);
            }
        }
    }
}
